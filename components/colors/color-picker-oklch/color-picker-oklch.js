        this.store['s' + (s + 2)] ||= 'oklch(.51 0 0)';                 // black lightness - 40%
        this.store['s' + (s + 3)] ||= 'oklch(.68 0 0)';                 // black lightness - 60%
        this.store['s' + (s + 4)] ||= 'oklch(.85 0 0)';                 // black lightness - 80%
